'use client';

// ============================================================================
// 设备故障排查提示组件
// ============================================================================

/**
 * 设备故障排查提示组件
 *
 * 功能：
 * - 根据错误类型展示解决方案
 * - 提供清晰的操作指引
 */

import React from 'react';
import { Alert } from 'antd';
import { InfoCircleOutlined } from '@ant-design/icons';

// ============================================================================
// 类型定义
// ============================================================================

interface TroubleshootTipsProps {
  /** 错误信息 */
  error: string | null;
}

// ============================================================================
// 组件实现
// ============================================================================

export function TroubleshootTips({ error }: TroubleshootTipsProps) {
  // 没有错误时不显示
  if (!error) return null;

  // --------------------------------------------------------------------------
  // 渲染
  // --------------------------------------------------------------------------

  return (
    <Alert
      message="设备连接提示"
      description={error}
      type="warning"
      icon={<InfoCircleOutlined />}
      showIcon
      style={{
        borderRadius: 12,
      }}
    />
  );
}
